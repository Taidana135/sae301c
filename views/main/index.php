<div class="qui-sommes-nous">
  <h2>Qui sommes-nous ?</h2>
  <p>
    L'ÉPISE (Épicerie Solidaire des Étudiants) est une initiative de l'Université de la Nouvelle-Calédonie,
    créée en 2016 pour soutenir les étudiants en situation de précarité. Située sur le campus de Nouville,
    elle propose, contre une cotisation symbolique de 200 FCFP, des produits essentiels — alimentaires et d'hygiène
    — gratuitement ou à prix réduits. Gérée par des bénévoles,
    l'ÉPISE vise à favoriser l'accès à une alimentation saine et équilibrée tout en s'inscrivant dans une démarche de solidarité et de durabilité pour améliorer la vie étudiante.
  </p>

